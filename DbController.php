/**
 * Connects to Database
 *
 * @author Debanik
 */

class DbController {
    function connect(mysqli $db){
        require_once __DIR__.'/config.php';

        $con = new mysqli(SERVER, USERNAME, PASSWORD, $db);
        if($con->errno!=0){
            return null;
        }
        return $con;
    }
    function disconnect(mysqli $con){
        $con->close();
    }
}

